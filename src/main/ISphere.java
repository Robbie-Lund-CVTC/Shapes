package main;

/**
 * The Interface ISphere.
 */
public interface ISphere {

	/**
	 * Sets the diameter.
	 *
	 * @param diameter
	 *            the new diameter
	 */
	public void setDiameter(float diameter);

	/**
	 * Gets the diameter.
	 *
	 * @return the diameter
	 */
	public float getDiameter();

	/**
	 * Sets the radius.
	 *
	 * @param radius
	 *            the new radius
	 */
	public void setRadius(float radius);

	/**
	 * Gets the radius.
	 *
	 * @return the radius
	 */
	public float getRadius();
}
