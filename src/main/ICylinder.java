package main;

/**
 * The Interface ICylinder.
 */
public interface ICylinder {

	/**
	 * Sets the diameter.
	 *
	 * @param diameter
	 *            the new diameter
	 */
	public void setDiameter(float diameter);

	/**
	 * Gets the diameter.
	 *
	 * @return the diameter
	 */
	public float getDiameter();

	/**
	 * Sets the height.
	 *
	 * @param height
	 *            the new height
	 */
	public void setHeight(float height);

	/**
	 * Gets the height.
	 *
	 * @return the height
	 */
	public float getHeight();

	/**
	 * Sets the radius.
	 *
	 * @param radius
	 *            the new radius
	 */
	public void setRadius(float radius);

	/**
	 * Gets the radius.
	 *
	 * @return the radius
	 */
	public float getRadius();
}
