package main;

/**
 * The Class Shape.
 */
abstract class Shape {

	/**
	 * Instantiates a new shape.
	 */
	public Shape() {
	}

	/**
	 * Surface area.
	 *
	 * @return the float
	 */
	abstract public float SurfaceArea();

	/**
	 * Volume.
	 *
	 * @return the float
	 */
	abstract public float Volume();

}
