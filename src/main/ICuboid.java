package main;

/**
 * The Interface ICuboid.
 */
public interface ICuboid {

	/**
	 * Sets the height.
	 *
	 * @param height
	 *            the new height
	 */
	public void setHeight(float height);

	/**
	 * Gets the height.
	 *
	 * @return the height
	 */
	public float getHeight();

	/**
	 * Sets the length.
	 *
	 * @param length
	 *            the new length
	 */
	public void setLength(float length);

	/**
	 * Gets the length.
	 *
	 * @return the length
	 */
	public float getLength();

	/**
	 * Sets the width.
	 *
	 * @param width
	 *            the new width
	 */
	public void setWidth(float width);

	/**
	 * Gets the width.
	 *
	 * @return the width
	 */
	public float getWidth();
}
